# for loop: iterate over a sequence such as a list, tuple, string, or range and perform an action for each item in the sequence.
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit) #goes through each string/fruit in the list and shows each one
    
numbers = [1,2,3,5,7]

for number in numbers:
    print(number) #goes through each value in the list and shows each one


# while loop: used to execute a block of code as long a certain condition is true
# using the while loop to count from 1 to 5
count = 1

while count <= 5:
    print(count)
    count += 1 # increments the count by 1 as long as count<=5
    
